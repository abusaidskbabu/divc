                                    <h2> GUIDELINES FOR&nbsp; </h2>
                                    <h2> VENTURE CAPITAL FUND MANAGEMENT&nbsp; </h2>
                                    <h2> UNDER IDEA PROJECT </h2>
                                    <p> MISSION STATEMENT </p>
                                    <p>Bangladesh is one of the youngest countries in the world, with more than half of its population being under the age of 25. The nation is transitioning towards becoming a middle-income country by year 2021. In order to develop an innovation-centered economy and sustain its remarkable growth, Government of Bangladesh (GoB) has undertaken a pioneering initiative to create a national entrepreneurship platform and its supporting ecosystem. This effort will enable the nation to innovate faster, create new jobs, develop technical skills and realize the vision of Digital Bangladesh.&nbsp;&nbsp; In support of the above-mentioned objectives, GoB has created a fund (the Fund) under iDEA project.&nbsp; The Fund will provide financial support to entrepreneurs in the form of equity, convertible debt and/or grant (the Investment). The Investment will provide the necessary capital to accelerate development and achieve success.&nbsp;&nbsp; The Fund is committed to fostering innovation and entrepreneurship through a process that is equitable, transparent and accountable.</p>
                                    <p> INVESTMENT OBJECTIVES </p>
                                    <p>The investment objectives are:</p>
                                    <ol>
                                       <li>Support technology-based innovation</li>
                                       <li>Create new employment opportunities</li>
                                       <li>Provide training and develop technical skills</li>
                                       <li>Promote groups that are under-represented in the tech sector</li>
                                       <li>Connect Non-Resident Bangladeshis (NRBs) with the local ecosystem</li>
                                       <li>Foster an entrepreneurship culture in Bangladesh</li>
                                       <li>Attract foreign investment and expertise</li>
                                    </ol>
                                    <p> INVESTMENT RISKS </p>
                                    <p>Venture investment carries high inherent risks. All submitted proposals will be assessed for certain major risks that include but are not limited to:</p>
                                    <ol>
                                       <li>Market Risks: market receptiveness to product/service; size of potential customer base; competitive dynamics &amp; pace of competing innovations; scalability of product/service</li>
                                       <li>2) Management and Execution Risks: strength and experience of management and technical teams</li>
                                       <li>Financial Risks: ramp-up period; burn-rate, profitability; capital required to operate; additional capital required to scale and distribute</li>
                                       <li>Other Risks: Additional risks may pertain to internal factors (i.e., legal, technical, operational) as well as external considerations (regulatory compliance, economic conditions)</li>
                                    </ol>
                                    <p>Despite the risks, Venture Capital investment takes place in dynamic economies worldwide because of the potential rewards – business success, technical breakthrough, skill development, employment generation, public benefits, international branding and so on.</p>
                                    <p> INVESTMENT CATEGORIES </p>
                                    <p>The following types of Investments may be made:</p>
                                    <ul>
                                       <li>IDEA (PRE-SEED) SUPPORT</li>
                                    </ul>
                                    <ul>
                                       <li>VENTURE CAPITAL</li>
                                    </ul>
                                    <p>a: Seed stage</p>
                                    <p>b: Growth stage</p>
                                    <p>c: Guided startups</p>
                                    <ul>
                                       <li>TARGETED INVESTMENTS</li>
                                    </ul>
                                    <ul>
                                       <li>STRATEGIC PARTNERSHIPS</li>
                                    </ul>
                                    <ul>
                                       <li>CO-INVESTMENTS</li>
                                    </ul>
                                    <p>Detailed policies and procedures for each category of Investment are discussed below:</p>
                                    <ul>
                                       <li> IDEA (PRE-SEED) SUPPORT </li>
                                    </ul>
                                    <p>The Fund will support innovative ideas thorough mentoring and funding:</p>
                                    <ol>
                                       <li>Requirement: Viable business idea and an executable business plan</li>
                                       <li>Investment amount: up to Tk. 10 lakh</li>
                                       <li>The Investment will be provided in tranches based on achievement of defined milestones</li>
                                       <li>Fund participation: Grant</li>
                                    </ol>
                                    <p>The Fund will require regular progress reports and financial statements for performance monitoring purposes.</p>
                                    <ul>
                                       <li> VENTURE CAPITAL </li>
                                    </ul>
                                    <p>The Fund will make the following kinds of Venture Capital investments:</p>
                                    <p>a :&nbsp;Seed stage</p>
                                    <ol>
                                       <li>Requirement: Prototype and an executable business plan</li>
                                       <li>Investment amount: up to Tk. 1 crore</li>
                                       <li>The Investment will be provided in tranches based on achievement of defined milestones</li>
                                       <li>The Investment may be used for product development, operations, expansion, marketing and other approved purposes</li>
                                       <li>Fund participation: Equity and/or Convertible Debt. The equity ownership percentage may be up to 49%, based on Investment amount and company valuation. In case of Convertible Debt, the amount financed will be interest-free. The Convertible Debt may be fully or partially convertible</li>
                                       <li>The Fund may have representation on the Board commensurate to its equity ownership</li>
                                       <li>The Fund will require regular progress reports and financial statements for performance monitoring purposes.</li>
                                    </ol>
                                    <p>b :&nbsp;Growth stage</p>
                                    <ol>
                                       <li>Requirement: Minimum Viable Product, existing customer base and an executable business plan</li>
                                       <li>Investment amount: up to Tk. 5 crore per round</li>
                                       <li>The Investment will be provided in tranches based on achievement of defined milestones</li>
                                       <li>The Investment may be used for product development, operations, expansion, marketing and other approved purposes</li>
                                       <li>The Investment may be made during one of the financing rounds generally known as Series A, Series B and Series C rounds</li>
                                       <li>Fund participation: Equity and/or Convertible Debt. The equity ownership percentage may be up to 49%, based on company valuation and Investment amount. In case of Convertible Debt, the amount financed will be interest-free. The Convertible Debt may be fully or partially convertible</li>
                                       <li>The Fund may have representation on the Board commensurate to its equity ownership</li>
                                       <li>The Fund will require regular progress reports and financial statements for performance monitoring purposes</li>
                                    </ol>
                                    <p>c :&nbsp;Guided Startups</p>
                                    <ol>
                                       <li>Requirement: Specific startup ideas that the Fund considers to be in the national interest</li>
                                       <li>Investment amount: up to Tk. 5 crore per round</li>
                                       <li>The Investment will be provided in tranches based on achievement of defined milestones</li>
                                       <li>The Investment may be used for product development, operations, expansion, marketing and other approved purposes</li>
                                       <li>Fund participation: Equity and/or Convertible Debt. The equity ownership percentage may be up to 49%, based on company valuation and Investment amount. In case of Convertible Debt, the amount financed will be interest-free. The Convertible Debt may be fully or partially convertible</li>
                                       <li>The Fund may have representation on the Board commensurate to its equity ownership</li>
                                       <li>The Fund will require regular progress reports and financial statements for performance monitoring purposes.</li>
                                    </ol>
                                    <ul>
                                       <li> TARGETED INVESTMENTS </li>
                                    </ul>
                                    <p>The Fund may make investments to create, support, augment or procure specific products, services, applications, and hardware or technology platforms. The Fund may also invest in training programs to create skilled resource pools in selected technology areas. Additionally, the Fund may make strategic investments in selected entities, projects or initiatives to bring about substantial public benefits. The investments may be made solely, in collaboration with other entities or following the Public-Private partnership (PPP) model.</p>
                                    <ul>
                                       <li> STRATEGIC PARTNERSHIPS </li>
                                    </ul>
                                    <p>The Fund may invest in strategic partnerships with local and international institutions (universities, companies, governments, incubators/accelerators, research organizations, media and other suitable entities) to acquire expertise, technology, products, services and/or support entrepreneurship development.</p>
                                    <ul>
                                       <li> CO-INVESTMENTS </li>
                                    </ul>
                                    <p>The Fund may enter into joint investments with other venture capital funds or funding entities in order to scale its investments and leverage partner expertise while sharing risks.&nbsp; Investments may be made in a single company, in a venture fund or in a fund-of-funds.</p>
                                    <p>Fund participation: Equity and/or Convertible Debt. The equity ownership percentage will be based on company valuation and Investment amount. In the case of Convertible Debt, the amount financed by GoB will be interest-free. The Convertible Debt may be fully or partially convertible.</p>
                                    <p>The Fund may have representation on the Board commensurate to its equity ownership.</p>
                                    <p>The Fund will require regular progress reports and financial statements for performance monitoring purposes.</p>
                                    <p> INVESTMENT AREAS </p>
                                    <p>The Fund will prioritize technical innovation in the following areas:</p>
                                    <ol>
                                       <li>Health</li>
                                       <li>Education</li>
                                       <li>Financial Services</li>
                                       <li>Environment</li>
                                       <li>Transportation</li>
                                       <li>Infrastructure</li>
                                       <li>Agriculture</li>
                                       <li>Food Processing</li>
                                       <li>Pharmaceutical</li>
                                       <li>Manufacturing</li>
                                       <li>Tourism</li>
                                       <li>Media &amp; Entertainment</li>
                                       <li>eGovernment/mGovernment</li>
                                       <li>eCommerce</li>
                                       <li>Biotech</li>
                                       <li>Robotics</li>
                                       <li>Semiconductor/Nanotech/IoT</li>
                                       <li>Other suitable sectors</li>
                                    </ol>
                                    <p> SELECTION CRITERIA </p>
                                    <p>Investment proposals will be selected for funding based on the following criteria:</p>
                                    <ol>
                                       <li>Business Concept</li>
                                       <li>Feasibility</li>
                                       <li>Strategy (business, technical, financial)</li>
                                       <li>Implementation Plan</li>
                                       <li>Management team</li>
                                       <li>Other factors relevant for business success</li>
                                    </ol>
                                    <p> RESPONSIBILITIES OF FUND RECIPIENTS </p>
                                    <p>The responsibilities of fund recipients will be as follows:</p>
                                    <ol>
                                       <li>Attend all training/mentoring sessions and other required events</li>
                                       <li>Ensure appropriate use of funds as per approved budget</li>
                                       <li>Maintain accurate business, technical and financial records</li>
                                       <li>Maintain registers and vouchers</li>
                                       <li>Maintain a separate business bank account for iDEA funds</li>
                                       <li>Provide regular progress reports and financial statements</li>
                                       <li>Return unspent funds and other resources upon completion, if requested by iDEA project authority</li>
                                       <li>Any other duties or responsibilities assigned by iDEA project authority</li>
                                    </ol>
                                    <p> REPORTING AND RECORDKEEPING </p>
                                    <p>The Fund will maintain necessary records documenting the success or failure of each investment. The Fund will also create appropriate reports and dashboards highlighting performance, utilization and impact.</p>
                                    <p>The performance reports will be presented to the Advisory Board for necessary evaluation and action.</p>
                                    <p> OWNERSHIP AND REVIEW OF THE GUIDELINES </p>
                                    <p>The ownership of this document lies with Bangladesh Computer Council</p>
                                    <p>The guidelines may be amended by Bangladesh Computer Council as deemed necessary. During its application, if any clarification is required, the request may be forwarded to iDEA project authority.</p>
